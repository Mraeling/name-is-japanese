def japaneseName(name):

	l = len(name)
	if l == 0:
		return False

	# set to lower:
	name = name.lower()

	vowel = ['a','e','i','o','u']

	cons = ['k','s','t','n','h','m','y','r','w','g','z','d','b','c','f','j','p']


	for i in range(0,l):
#		print("analyse: ",name[i])
		c = name[i] # current letter
		if c in vowel or c == "n" or c == " ":
			continue
		elif c in cons and i+1 < l:
			if name[i+1] in vowel or name[i+1] == name[i] or name[i-1] == name[i]:
				continue
